#include<stdio.h>

int main()
{
    char t;
    scanf("%c",&t);
    switch (t)
    {
    case 'a':
        printf("Apple");
        break;
    case 'b':
        printf("Banana");
        break;
    case 'c':
        printf("Cherry");
        break;
    case 'd':
        printf("Durian");
        break;
    case 'e':
        printf("Egg");
        break;
    case 'f':
        printf("Fish");
        break;
    case 'A':
        printf("Apple");
        break;
    case 'B':
        printf("Banana");
        break;
    case 'C':
        printf("Cherry");
        break;
    case 'D':
        printf("Durian");
        break;
    case 'E':
        printf("Egg");
        break;
    case 'F':
        printf("Fish");
        break;
    
    default:
        printf("Error");
        break;
    }
    return 0;
}